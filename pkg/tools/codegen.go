// CodeGeneratorTool generates Go boilerplate code from templates.
type CodeGeneratorTool struct {
	outputDir string
}

// NewCodeGeneratorTool creates a new code generator tool.
func NewCodeGeneratorTool(outputDir string) *CodeGeneratorTool {
	return &CodeGeneratorTool{
		outputDir: outputDir,
	}
}

// Name returns the tool name.
func (t *CodeGeneratorTool) Name() string {
	return "codegen"
}

// Description returns the tool description.
func (t *CodeGeneratorTool) Description() string {
	return "Generate Go boilerplate code from templates (api, service, handler, model, config)"
}

// Parameters returns the tool parameters.
func (t *CodeGeneratorTool) Parameters() map[string]any {
	return map[string]any{
		"type": map[string]any{
			"type":        "string",
			"description": "Type of code to generate: api, service, handler, model, config",
			"enum":        []string{"api", "service", "handler", "model", "config"},
		},
		"name": map[string]any{
			"type":        "string",
			"description": "Name of the component (e.g., UserService, OrderHandler)",
		},
		"package": map[string]any{
			"type":        "string",
			"description": "Package name (default: derived from name)",
			"default":     "",
		},
		"output": map[string]any{
			"type":        "string",
			"description": "Output directory (default: ./generated)",
			"default":     "",
		},
	}
}

// Execute generates the code.
func (t *CodeGeneratorTool) Execute(ctx context.Context, args map[string]any) *ToolResult {
	codeType, ok := args["type"].(string)
	if !ok {
		return ErrorResult("type is required and must be a string")
	}

	name, ok := args["name"].(string)
	if !ok {
		return ErrorResult("name is required and must be a string")
	}

	pkgName, _ := args["package"].(string)
	outputDir, _ := args["output"].(string)

	if outputDir == "" {
		outputDir = t.outputDir
	}
	if outputDir == "" {
		outputDir = "./generated"
	}

	if pkgName == "" {
		pkgName = strings.ToLower(extractBaseName(name))
	}

	// Generate code based on type
	var code string
	var filename string

	switch codeType {
	case "api":
		code, filename = t.generateAPI(name, pkgName)
	case "service":
		code, filename = t.generateService(name, pkgName)
	case "handler":
		code, filename = t.generateHandler(name, pkgName)
	case "model":
		code, filename = t.generateModel(name, pkgName)
	case "config":
		code, filename = t.generateConfig(name, pkgName)
	default:
		return ErrorResult("unknown code type: " + codeType)
	}

	// Create output directory
	if err := os.MkdirAll(outputDir, 0o755); err != nil {
		return ErrorResult("creating output directory: " + err.Error())
	}

	// Write file
	outputPath := filepath.Join(outputDir, filename)
	if err := os.WriteFile(outputPath, []byte(code), 0o644); err != nil {
		return ErrorResult("writing file: " + err.Error())
	}

	return UserResult(fmt.Sprintf("Generated %s at %s", filename, outputPath))
}

