// extractBaseName extracts the base name from a compound name.
// E.g., "UserService" -> "User", "OrderHandler" -> "Order"
func extractBaseName(name string) string {
	// Find common suffixes
	suffixes := []string{"Service", "Handler", "API", "Config", "Manager", "Controller"}

	for _, suffix := range suffixes {
		if strings.HasSuffix(name, suffix) {
			return strings.TrimSuffix(name, suffix)
		}
	}

	return name
}
